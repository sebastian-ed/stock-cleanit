create or replace function public.audit_stock_change() returns trigger language plpgsql security definer set search_path=public as $$
declare
  v_reporter text := nullif(current_setting('app.reporter_name',true),'');
begin
  if tg_op='INSERT' then
    insert into public.stock_history(service_id,material_id,old_quantity,new_quantity,changed_by,reporter_name)
    values(new.service_id,new.material_id,null,new.quantity,coalesce(new.updated_by,auth.uid()),v_reporter);
  elsif old.quantity is distinct from new.quantity then
    insert into public.stock_history(service_id,material_id,old_quantity,new_quantity,changed_by,reporter_name)
    values(new.service_id,new.material_id,old.quantity,new.quantity,coalesce(new.updated_by,auth.uid()),v_reporter);
  end if;
  return new;
end; $$;

drop trigger if exists service_stock_audit on public.service_stock;
