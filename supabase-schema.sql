-- Lectura pública controlada. No expone perfiles, historial ni notas internas de servicios.
create or replace function public.public_inventory_bootstrap(p_service_id uuid default null)
returns jsonb
language plpgsql
stable
security definer
set search_path=public
as $$
declare
  v_services jsonb;
  v_materials jsonb;
  v_stocks jsonb := '[]'::jsonb;
  v_extras jsonb := '[]'::jsonb;
begin
  if p_service_id is not null and not exists(
    select 1 from public.services where id=p_service_id and active=true
  ) then
    raise exception 'El servicio seleccionado no existe o está inactivo.';
  end if;

  select coalesce(jsonb_agg(to_jsonb(x) order by x.name),'[]'::jsonb)
  into v_services
  from (
    select id,name,address,description,active
    from public.services
    where active=true
  ) x;

  select coalesce(jsonb_agg(to_jsonb(x) order by x.category,x.sort_order,x.name),'[]'::jsonb)
  into v_materials
  from (
    select m.id,m.slug,m.name,m.category,m.detail,m.unit,m.control_type,m.image_url,m.critical_level,m.target_level,m.sort_order,m.active
    from public.materials m
    where m.active=true
      and (
        p_service_id is null
        or not exists(
          select 1
          from public.service_material_visibility smv
          where smv.service_id=p_service_id
            and smv.material_id=m.id
            and smv.enabled=false
        )
      )
  ) x;

  if p_service_id is not null then
    select coalesce(jsonb_agg(to_jsonb(x) order by x.updated_at desc),'[]'::jsonb)
    into v_stocks
    from (
      select ss.id,ss.service_id,ss.material_id,ss.quantity,ss.condition_status,ss.notes,ss.updated_at
      from public.service_stock ss
      join public.materials m on m.id=ss.material_id and m.active=true
      where ss.service_id=p_service_id
        and not exists(
          select 1
          from public.service_material_visibility smv
          where smv.service_id=p_service_id
            and smv.material_id=ss.material_id
            and smv.enabled=false
        )
    ) x;

    select coalesce(jsonb_agg(to_jsonb(x) order by x.name),'[]'::jsonb)
    into v_extras
    from (
      select id,service_id,name,unit,quantity,notes,image_url,critical_level,target_level,submitted_by,active,created_at,updated_at
      from public.service_extra_stock
      where service_id=p_service_id and active=true
    ) x;
  end if;

  return jsonb_build_object(
    'services',v_services,
    'materials',v_materials,
    'stocks',v_stocks,
    'extra_stocks',v_extras
  );
end;
$$;

-- Guarda un relevamiento público sin conceder acceso anónimo directo a las tablas.
create or replace function public.public_submit_inventory(
  p_service_id uuid,
  p_stock_items jsonb,
  p_extra_items jsonb default '[]'::jsonb,
  p_reporter_name text default 'Operario sin identificar'
)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  v_item jsonb;
  v_material_id uuid;
  v_extra_id uuid;
  v_quantity numeric(12,2);
  v_condition text;
  v_control_type text;
  v_reporter text := left(coalesce(nullif(btrim(p_reporter_name),''),'Operario sin identificar'),100);
  v_user uuid;
  v_count integer := 0;
begin
  if not exists(select 1 from public.services where id=p_service_id and active=true) then
    raise exception 'El servicio seleccionado no existe o está inactivo.';
  end if;
  if jsonb_typeof(coalesce(p_stock_items,'[]'::jsonb)) <> 'array' or jsonb_typeof(coalesce(p_extra_items,'[]'::jsonb)) <> 'array' then
    raise exception 'El formato del relevamiento no es válido.';
  end if;
  if jsonb_array_length(coalesce(p_stock_items,'[]'::jsonb)) > 250 or jsonb_array_length(coalesce(p_extra_items,'[]'::jsonb)) > 100 then
    raise exception 'El relevamiento supera la cantidad permitida de registros.';
  end if;

  select id into v_user from public.profiles where id=auth.uid();
  perform set_config('app.reporter_name',v_reporter,true);

  for v_item in select value from jsonb_array_elements(coalesce(p_stock_items,'[]'::jsonb)) loop
    v_material_id := (v_item->>'material_id')::uuid;
    v_quantity := greatest(0,least(999999,coalesce((v_item->>'quantity')::numeric,0)));
    select m.control_type into v_control_type
    from public.materials m
    where m.id=v_material_id
      and m.active=true
      and not exists(
        select 1
        from public.service_material_visibility smv
        where smv.service_id=p_service_id
          and smv.material_id=m.id
          and smv.enabled=false
      );

    if found then
      v_condition := nullif(v_item->>'condition_status','');
      if v_control_type <> 'quantity_condition' or coalesce(v_condition,'') not in('good','used','replace') then
        v_condition := null;
      end if;

      insert into public.service_stock(service_id,material_id,quantity,condition_status,updated_by,updated_at)
      values(p_service_id,v_material_id,v_quantity,v_condition,v_user,now())
      on conflict(service_id,material_id) do update set
        quantity=excluded.quantity,
        condition_status=excluded.condition_status,
        updated_by=excluded.updated_by,
        updated_at=now();
      v_count := v_count + 1;
    end if;
  end loop;

  for v_item in select value from jsonb_array_elements(coalesce(p_extra_items,'[]'::jsonb)) loop
    v_extra_id := (v_item->>'id')::uuid;
    v_quantity := greatest(0,least(999999,coalesce((v_item->>'quantity')::numeric,0)));
    update public.service_extra_stock
    set quantity=v_quantity,submitted_by=v_reporter,updated_by=v_user,updated_at=now()
    where id=v_extra_id and service_id=p_service_id and active=true;
    if found then v_count := v_count + 1; end if;
  end loop;

  return jsonb_build_object('ok',true,'updated_records',v_count,'updated_at',now());
end;
$$;

-- Agrega o actualiza un insumo no catalogado, limitado al servicio elegido.
create or replace function public.public_add_extra_stock(
  p_service_id uuid,
  p_name text,
  p_quantity numeric,
  p_unit text default 'unidad',
  p_notes text default null,
  p_reporter_name text default 'Operario sin identificar'
)
returns jsonb
language plpgsql
security definer
set search_path=public
as $$
declare
  v_id uuid;
  v_name text := left(btrim(coalesce(p_name,'')),120);
  v_unit text := left(coalesce(nullif(btrim(p_unit),''),'unidad'),60);
  v_notes text := nullif(left(btrim(coalesce(p_notes,'')),180),'');
  v_quantity numeric(12,2) := greatest(0,least(999999,coalesce(p_quantity,0)));
  v_reporter text := left(coalesce(nullif(btrim(p_reporter_name),''),'Operario sin identificar'),100);
  v_user uuid;
  v_result jsonb;
begin
  if not exists(select 1 from public.services where id=p_service_id and active=true) then
    raise exception 'El servicio seleccionado no existe o está inactivo.';
  end if;
  if char_length(v_name) < 2 then raise exception 'Ingresá un nombre válido para el insumo.'; end if;
  if exists(select 1 from public.materials where active=true and lower(btrim(name))=lower(v_name)) then
    raise exception 'Ese insumo ya existe en el catálogo precargado.';
  end if;

  select id into v_user from public.profiles where id=auth.uid();
  perform set_config('app.reporter_name',v_reporter,true);

  select id into v_id
  from public.service_extra_stock
  where service_id=p_service_id and lower(btrim(name))=lower(v_name)
  limit 1;

  if v_id is null then
    insert into public.service_extra_stock(service_id,name,unit,quantity,notes,submitted_by,updated_by,active)
    values(p_service_id,v_name,v_unit,v_quantity,v_notes,v_reporter,v_user,true)
    returning id into v_id;
  else
    update public.service_extra_stock
    set name=v_name,unit=v_unit,quantity=v_quantity,notes=v_notes,submitted_by=v_reporter,updated_by=v_user,active=true,updated_at=now()
    where id=v_id;
  end if;

  select to_jsonb(x) into v_result
  from (
    select id,service_id,name,unit,quantity,notes,image_url,critical_level,target_level,submitted_by,active,created_at,updated_at
    from public.service_extra_stock where id=v_id
  ) x;
  return v_result;
end;
$$;

