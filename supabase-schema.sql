create or replace function public.audit_extra_stock_change() returns trigger language plpgsql security definer set search_path=public as $$
declare
  v_reporter text;
begin
  v_reporter := coalesce(nullif(current_setting('app.reporter_name',true),''),new.submitted_by);
  if tg_op='INSERT' then
    insert into public.extra_stock_history(extra_stock_id,service_id,extra_name,old_quantity,new_quantity,changed_by,reporter_name)
    values(new.id,new.service_id,new.name,null,new.quantity,coalesce(new.updated_by,auth.uid()),v_reporter);
  elsif old.quantity is distinct from new.quantity then
    insert into public.extra_stock_history(extra_stock_id,service_id,extra_name,old_quantity,new_quantity,changed_by,reporter_name)
    values(new.id,new.service_id,new.name,old.quantity,new.quantity,coalesce(new.updated_by,auth.uid()),v_reporter);
  end if;
  return new;
end; $$;

