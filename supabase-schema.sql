create table if not exists public.services(
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  address text,
  description text,
  notes text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.services add column if not exists description text;

create table if not exists public.materials(
  id uuid primary key default gen_random_uuid(),
  slug text not null unique,
  name text not null,
  category text not null,
  detail text,
  unit text not null default 'unidad',
  control_type text not null default 'integer' check(control_type in('integer','fractional','quantity_condition')),
  image_url text,
  critical_level numeric(12,2) not null default 0 check(critical_level>=0),
  target_level numeric(12,2) not null default 1 check(target_level>=critical_level),
  sort_order integer not null default 100,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.materials add column if not exists control_type text not null default 'integer';
alter table public.materials drop constraint if exists materials_control_type_check;
alter table public.materials add constraint materials_control_type_check check(control_type in('integer','fractional','quantity_condition'));

create table if not exists public.profiles(
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  role text not null default 'operator' check(role in('admin','operator')),
  service_id uuid references public.services(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.service_stock(
  id uuid primary key default gen_random_uuid(),
  service_id uuid not null references public.services(id) on delete cascade,
  material_id uuid not null references public.materials(id) on delete cascade,
  quantity numeric(12,2) not null default 0 check(quantity>=0),
  condition_status text check(condition_status is null or condition_status in('good','used','replace')),
  notes text,
  updated_by uuid references public.profiles(id) on delete set null,
  updated_at timestamptz not null default now(),
  unique(service_id,material_id)
);


